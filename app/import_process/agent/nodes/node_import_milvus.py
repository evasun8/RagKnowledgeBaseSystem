def step_4_insert_data(client, chunks_json_data: List[Dict[str, Any]]) -> List[Dict[str, Any]]: 
    """
    Step 4: Batch-insert chunk data into Milvus and backfill primary keys
    
    Core Logic:
    1. Remove manual chunk_ids: Since auto_id=True is enabled, Milvus automatically generates the primary keys. Removing them avoids collisions.
    2. Batch-insert data: Boosts persistence insertion throughput efficiency and minimizes Milvus transactional connection overhead.
    3. Backfill chunk_ids: Backfills the auto-incremented primary keys generated by Milvus into the chunk records for downstream business usage.
    
    Parameters:
    client - Connected MilvusClient instance.
    chunks_json_data: List[Dict[str, Any]] - The payload list of chunks awaiting database insertion.
    
    Returns:
    List[Dict[str, Any]] - The list of chunks now enriched with backfilled 'chunk_id' fields.
    """ 
    # 1. Preprocess data: Strip any manual chunk_id fields to prevent conflicts with the Milvus auto-increment primary key
    data_to_insert = [] 
    for item in chunks_json_data: 
        item_copy = item.copy() 
        if isinstance(item_copy, dict) and "chunk_id" in item_copy: 
            item_copy.pop("chunk_id", None) 
        data_to_insert.append(item_copy) 
        
    logger.info(f"Milvus data insertion: Prepared {len(data_to_insert)} chunk records. Starting batch insertion.") 
    
    # 2. Execute batch insertion transaction mutation
    insert_result = client.insert(collection_name=CHUNKS_COLLECTION_NAME, data=data_to_insert) 
    insert_count = insert_result.get('insert_count', 0) 
    logger.info(f"Milvus data insertion completed: Successfully inserted {insert_count} records. Insertion receipt metadata: {insert_result}") 
    
    # 3. Primary key backfilling: Map the auto-generated chunk_ids returned by Milvus back to the original source chunk instances
    inserted_ids = insert_result.get('ids', []) 
    if inserted_ids and len(inserted_ids) == len(chunks_json_data): 
        logger.info(f"Milvus primary key backfilling: Starting to write {len(inserted_ids)} auto-incremented chunk_ids back into chunk memory records.") 
        for idx, item in enumerate(chunks_json_data): 
            item['chunk_id'] = str(inserted_ids[idx]) 
        logger.info("Milvus primary key backfilling completed: All chunk objects are now bound with persistent chunk_ids.") 
    else: 
        logger.warning(f"Milvus primary key backfilling failed: The number of generated database IDs ({len(inserted_ids)}) does not match the input chunk array payload count ({len(chunks_json_data)}).") 
        
    return chunks_json_data

def node_import_milvus(state: Dict[str, Any]) -> Dict[str, Any]: 
    """
    LangGraph Core Node: Main Pipeline for Milvus Chunk Data Persistence
    
    Execution Pipeline (Sequential execution with step-by-step verification to ensure data consistency):
    1. Input Validation: Verifies chunk viability and vector field integrity, extracting vector dimensional depths.
    2. Environment Provisioning: Connects to Milvus and auto-creates Schema + Indexes if the collection does not exist.
    3. Idempotent Cleanup: Purges historical records sharing the same 'item_name' to prevent duplicate records.
    4. Batch Insertion: Preprocesses data for batch persistence, backfilling Milvus-generated auto-incremented 'chunk_id' fields.
    5. State Synchronization: Flushes the chunk_id-enriched chunks back into the global state for consumption by downstream nodes.
    
    Parameters:
    state: Dict[str, Any] - The global pipeline state object containing chunks, task_id, and other data payload properties.
    
    Returns:
    Dict[str, Any] - The updated state object, where the chunks field now contains backfilled 'chunk_id' metrics.
    
    Exception Handling:
    Any failure within individual steps will raise a ValueError, terminating node execution to safeguard against dirty data writes.
    """ 
     # Retrieve the current node name for task monitoring and logging signatures
    current_node = sys._getframe().f_code.co_name 
    logger.info(f">>> Starting execution of LangGraph node: {current_node} (Milvus Chunk Data Persistence)") 
    
    # Mark task execution status for frontend progress dashboard displays and task monitoring tracking
    add_running_task(state["task_id"], current_node) 
    logger.info("--- Milvus Chunk Data Persistence Pipeline Triggered ---") 
    
    try: 
        # Step 1: Input data validity and payload structure verification
        chunks_json_data, vector_dimension = step_1_check_input(state) 
        
        # Step 2: Milvus client link orchestration + collection onboarding (Automated Table Creation)
        client = step_2_prepare_collection(vector_dimension) 
        
        # Step 3: Idempotency execution - Purging historical records matching the same 'item_name'
        step_3_clean_old_data(client, chunks_json_data) 
        
        # Step 4: Batch insertion of data payloads + primary key chunk_id backfilling mutations
        updated_chunks = step_4_insert_data(client, chunks_json_data) 
        
        # Step 5: Update the global state, passing backfilled chunk records to downstream consumers
        state["chunks"] = updated_chunks 
        logger.info("--- Milvus Chunk Data Persistence Pipeline Completed ---") 
        
    except Exception as e: 
        logger.error(f"Milvus chunk persistence node execution failed: {str(e)}", exc_info=True) 
        raise ValueError(f"An error occurred during the Milvus import pipeline: {e}") 
        
    return state

